// ─── Injection dans chaque fichier locale ───────────────────────────────────
const langs = ["fr", "en", "de", "es", "it"];

for (const lang of langs) {
  const filePath = resolve(LOCALES_DIR, lang, "common.json");
  const existing = JSON.parse(readFileSync(filePath, "utf-8"));
  const newKeys = translations[lang];

  // Vérifier si les clés existent déjà
  const alreadyExists = Object.keys(newKeys).every((k) => k in existing);
  if (alreadyExists) {
    console.log(`[${lang}] Clés déjà présentes — skip`);
    continue;
  }

  // Fusionner (les nouvelles clés à la fin)
  const merged = { ...existing, ...newKeys };
  writeFileSync(filePath, JSON.stringify(merged, null, 2) + "\n", "utf-8");
  console.log(`[${lang}] ${Object.keys(newKeys).length} clés ajoutées ✅`);
}

